package javajungsuck.chapter6;

public class A01_Mymath {
	long add(long a, long b) {
		long result = a + b;
		return result;
		//또는 return a+b;로 표현 가능
	}
	long subtract(long a, long b) {
		return a-b;
	}
	long multiply(long a, long b) {
		return a*b;
	}
	long divide(long a, long b) {
		return a/b;
	}
}

